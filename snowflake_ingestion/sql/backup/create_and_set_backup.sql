  FOR TABLE DW_NAME_PLACEHOLDER.SCHEMA_RAW_SCHEMA_PLACEHOLDER.RAW_TABLE_PLACEHOLDER
  WITH BACKUP POLICY RAW_TABLE_PLACEHOLDER_POLICY
  COMMENT = 'Backup set for table RAW_TABLE_PLACEHOLDER in schema RAW_SCHEMA_PLACEHOLDER';

